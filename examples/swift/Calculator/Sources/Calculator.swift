struct Calculator {

    func add(_ a: Int, _ b: Int) -> Int {
        return a + b
    }

    func sub(_ a: Int, _ b: Int) -> Int {
        return a - b
    }

    func mul(_ a: Int, _ b: Int) -> Int {
        return a * b
    }

    func div(_ a: Int, _ b: Int) -> Int {
        return a / b
    }
}
